# Slack and Telegram both call into ``dispatch()`` via thin reply callbacks.
# Slack mode can be triggered either from the Events API webhook (registered
# as ``/api/slack/events`` in routes.py) or from a one-off dispatch.

def _slack_reply(channel: str, thread_ts: Optional[str] = None):
    from . import slack_api

    def _wire(text: str) -> None:
        try:
            payload = {"channel": channel, "text": text[:38000]}
            if thread_ts:
                payload["thread_ts"] = thread_ts
            slack_api._call("chat.postMessage", payload)
        except Exception as e:
            log.warning("slack reply failed: %s", e)

    def _send(text: str) -> None:
        coalesced_reply("slack", channel, text, _wire)

    return _send


def _discord_reply(channel: str):
    from . import discord_api

    def _wire(text: str) -> None:
        try:
            discord_api.send_message(channel, text)
        except Exception as e:
            log.warning("discord reply failed: %s", e)

    def _send(text: str) -> None:
        coalesced_reply("discord", channel, text, _wire)

    return _send


def handle_discord_interaction(payload: dict) -> dict:
    """Discord Interactions API receiver.

    Expects the payload Discord sends to the configured interactions endpoint.
    Returns a response dict that the HTTP handler serializes back to the
    client. Type-1 PING gets a Type-1 PONG; Type-2 application command + Type-3
    component / message events route into the orchestrator.
    """
    if not isinstance(payload, dict):
        return {"type": 4, "data": {"content": "(bad payload)"}}
    itype = payload.get("type")
    if itype == 1:                         # PING
        return {"type": 1}
    # Application command (slash) or component interaction
    chan = ((payload.get("channel") or {}).get("id")
            or payload.get("channel_id") or "")
    user = ((payload.get("member") or {}).get("user")
            or payload.get("user") or {}).get("username", "")
    text = ""
    data = payload.get("data") or {}
    # Slash command "/ask <prompt>" — pull the first string option
    for opt in (data.get("options") or []):
        if isinstance(opt, dict) and opt.get("type") == 3 and opt.get("value"):
            text = str(opt["value"]); break
    # Fall back to a raw "name + first option text" if shape differs
    if not text:
        text = str(data.get("custom_id") or data.get("name") or "").strip()

    if not (chan and text):
        return {"type": 4, "data": {"content": "(missing channel or text)"}}

    binding = find_binding("discord", str(chan))
    if binding is None and os.environ.get("ORCH_DISCORD_BIND_REQUIRED", "1") != "0":
        return {"type": 4, "data": {"content": "(channel not bound)"}}

    threading.Thread(
        target=dispatch,
        kwargs={"text": text, "kind": "discord", "channel": str(chan),
                "user": user, "reply": _discord_reply(str(chan))},
        name=f"orch-dc-{chan}",
        daemon=True,
    ).start()
    # Type 5 = "deferred channel message" — Discord shows a "thinking…" state
    # while the orchestrator runs. Final reply lands via send_message.
    return {"type": 5}


def _telegram_reply(chat: str, reply_to: Optional[int] = None):
    from . import telegram_api

    def _wire(text: str) -> None:
        try:
            telegram_api.send_message(chat, text, reply_to=reply_to)
        except Exception as e:
            log.warning("telegram reply failed: %s", e)

    def _send(text: str) -> None:
        coalesced_reply("telegram", chat, text, _wire)

    return _send


def handle_telegram_update(upd: dict) -> None:
    """Wire-up for telegram_api.start_long_poll(handler=this)."""
    msg = upd.get("message") or upd.get("edited_message") or {}
    chat = (msg.get("chat") or {}).get("id")
    if chat is None:
        return
    chat_str = str(chat)
    text = (msg.get("text") or "").strip()
    if not text:
        return
    binding = find_binding("telegram", chat_str)
    if binding is None:
        # Honour explicit-binding mode by default — silent if not bound.
        if os.environ.get("ORCH_TELEGRAM_BIND_REQUIRED", "1") != "0":
            return
    user = ((msg.get("from") or {}).get("username")
            or (msg.get("from") or {}).get("first_name") or "")
    threading.Thread(
        target=dispatch,
        kwargs={"text": text, "kind": "telegram", "channel": chat_str,
                "user": user,
                "reply": _telegram_reply(chat_str, reply_to=msg.get("message_id"))},
        name=f"orch-tg-{chat_str}",
        daemon=True,
    ).start()


def handle_slack_event(event: dict) -> None:
    """Wire-up for the Slack Events API webhook."""
    inner = event.get("event") or event
    if inner.get("type") not in ("app_mention", "message"):
        return
    if inner.get("subtype") in ("bot_message", "message_changed", "message_deleted"):
        return
    text = (inner.get("text") or "").strip()
    channel = (inner.get("channel") or "").strip()
    if not (text and channel):
        return
    binding = find_binding("slack", channel)
    if binding is None and os.environ.get("ORCH_SLACK_BIND_REQUIRED", "1") != "0":
        return
    user = inner.get("user") or ""
    thread_ts = inner.get("thread_ts") or inner.get("ts")
    threading.Thread(
        target=dispatch,
        kwargs={"text": text, "kind": "slack", "channel": channel, "user": user,
                "reply": _slack_reply(channel, thread_ts=thread_ts)},
        name=f"orch-sl-{channel}",
        daemon=True,
    ).start()


# ───────── Recurrence sweeper (F2) ─────────
#
# Single 60-second tick walks every binding with a non-empty
# ``schedule.everyMinutes``. Bindings whose ``lastRunMs`` is more than
# ``everyMinutes * 60_000`` ms ago get fired with their canned prompt.
# A successful fire updates ``lastRunMs`` back into the binding so the
# next tick computes the right delta.
#
# Why a single sweeper rather than per-binding Timers: bindings come and
# go, and Timer.cancel() races are easy to get wrong. One sweeper, one
# scan per tick, O(N_bindings) where N is small. Lock-free against
# dispatch since each fired dispatch runs in its own daemon thread.

_SWEEP_INTERVAL_S = max(5, int(os.environ.get("ORCH_SWEEP_INTERVAL_S", "60")))
_SWEEP_LOCK = threading.Lock()
_SWEEP_THREAD: Optional[threading.Thread] = None
_SWEEP_STOP = threading.Event()


def _sweep_once() -> int:
    """One pass over the bindings. Returns the number of dispatches fired
    (used by tests + status endpoints).
    """
    cfg = load_config()
    bindings = cfg.get("bindings") or []
    if not bindings:
        return 0
    now_ms = int(time.time() * 1000)
    fired = 0
    dirty = False
    for b in bindings:
        sched = b.get("schedule")
        if not (isinstance(sched, dict) and sched.get("everyMinutes")
                and sched.get("prompt")):
            continue
        every_ms = int(sched["everyMinutes"]) * 60_000
        last = int(sched.get("lastRunMs") or 0)
        if now_ms - last < every_ms:
            continue
        # Fire — in a daemon thread so a stuck dispatch doesn't block the
        # next tick.
        kind = b.get("kind", "")
        channel = b.get("channel") or b.get("chat") or "default"
        prompt = sched["prompt"]
        reply = None
        if kind == "slack":
            reply = _slack_reply(channel)
        elif kind == "telegram":
            reply = _telegram_reply(channel)
        elif kind == "discord":
            reply = _discord_reply(channel)
        elif kind == "email" and isinstance(b.get("smtp"), dict):
            reply = _email_reply(b["smtp"], b.get("channel", "default"))
        threading.Thread(
            target=dispatch,
            kwargs={"text": prompt, "kind": kind, "channel": channel,
                    "user": "scheduled", "reply": reply},
            name=f"orch-sweep-{kind}-{channel}",
            daemon=True,
        ).start()
        sched["lastRunMs"] = now_ms
        dirty = True
        fired += 1
    if dirty:
        save_config(cfg)
    return fired


def _sweep_loop() -> None:
    while not _SWEEP_STOP.is_set():
        try:
            _sweep_once()
        except Exception as e:
            log.warning("orch sweeper crash: %s", e)
        if _SWEEP_STOP.wait(_SWEEP_INTERVAL_S):
            return


def start_sweeper() -> bool:
    """Idempotent. Returns True if the sweeper was already running or just
    started, False on failure (currently always True).
    """
    global _SWEEP_THREAD
    with _SWEEP_LOCK:
        if _SWEEP_THREAD is not None and _SWEEP_THREAD.is_alive():
            return True
        _SWEEP_STOP.clear()
        t = threading.Thread(target=_sweep_loop, name="orch-sweeper",
                             daemon=True)
        _SWEEP_THREAD = t
        t.start()
    return True


def stop_sweeper(timeout: float = 2.0) -> None:
    global _SWEEP_THREAD
    with _SWEEP_LOCK:
        _SWEEP_STOP.set()
        t = _SWEEP_THREAD
        _SWEEP_THREAD = None
    if t is not None:
        t.join(timeout=timeout)


def start_listeners() -> dict:
    """Idempotently spin up Telegram long-poll if a token is configured.

    Slack inbound is webhook-driven (Slack Events API) — no listener needed
    here. Returns a small status dict so callers (server.py boot, /api/orch/start)
    can render the result.
    """
    from . import telegram_api
    started_tg = telegram_api.start_long_poll(handle_telegram_update)
    sweeper_ok = start_sweeper()
    return {
        "telegram": "running" if started_tg else "skipped (no token)",
        "slack":    "webhook-mode",
        "sweeper":  f"running (every {_SWEEP_INTERVAL_S}s)" if sweeper_ok else "off",
    }


