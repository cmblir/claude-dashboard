def _binding_id(binding: Optional[dict]) -> str:
    """Stable directory-safe id for a binding. ``slack:Cxxxxx`` etc.

    Path-traversal segments (``..``, ``/``, leading ``.``) are normalised so
    the result is always safe to splice into a path, even if the binding
    target was attacker-controlled.
    """
    if not binding:
        return ""
    kind = binding.get("kind", "")
    target = (binding.get("channel") or binding.get("chat") or "default")
    safe = re.sub(r"[^A-Za-z0-9_-]+", "_", f"{kind}-{target}")
    safe = safe.lstrip(".-_") or "default"
    return safe[:120]


def _ensure_workspace(binding: Optional[dict]) -> dict:
    """Materialise per-binding workspace if missing. Returns
    ``{"cwd": "...", "claudeMdPath": "..."}`` or ``{}`` when isolation is off
    or the binding has no id (e.g. http: default).
    """
    if not binding:
        return {}
    bid = _binding_id(binding)
    if not bid:
        return {}
    root = Path(_AGENT_WORKSPACE_ROOT) / bid
    try:
        root.mkdir(parents=True, exist_ok=True)
        memdir = root / "memory"
        memdir.mkdir(exist_ok=True)
        cmdpath = root / "CLAUDE.md"
        if not cmdpath.exists():
            label = binding.get("label") or bid
            seed = (
                f"# Orchestrator workspace — {label}\n\n"
                f"This file is auto-generated by the LazyClaude orchestrator "
                f"for the binding `{bid}`. Edit freely — nothing here is "
                f"overwritten on subsequent dispatches.\n\n"
                f"## Channel context\n"
                f"- kind: `{binding.get('kind')}`\n"
                f"- target: `{binding.get('channel') or binding.get('chat') or '—'}`\n"
                f"- assignees: {', '.join(binding.get('assignees') or [])}\n"
            )
            cmdpath.write_text(seed, encoding="utf-8")
        return {"cwd": str(root), "claudeMdPath": str(cmdpath)}
    except Exception as e:
        log.warning("workspace setup failed for %s: %s", bid, e)
        return {}


